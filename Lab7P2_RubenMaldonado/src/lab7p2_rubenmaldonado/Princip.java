    private void jButton15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton15MouseClicked
        if (TableAlbum.getSelectedRow() >= 0) {
            row = TableAlbum.getSelectedRow();
            DefaultTableModel modelo
                    = (DefaultTableModel) TableAlbum.getModel();
            Object genero = modelo.getValueAt(TableAlbum.getSelectedRow(), 1);
            Object alias = modelo.getValueAt(TableAlbum.getSelectedRow(), 3);
            String n = genero.toString();
            String a = alias.toString();
            for (int i = 0; i < auxi.size(); i++) {
                String aux = ((Album) album.get(i)).getGenero();
                String aux2 = ((Album) album.get(i)).getAliass();
                if (n.equals(aux) && a.equals(aux2)) {
                    ArrayList temp = new ArrayList();
                    temp = ((Album) album.get(row)).getCanciones();
                    if (!temp.isEmpty()) {
                        DefaultTableModel modelo2
                                = (DefaultTableModel) TableCanciones.getModel();
                        for (int ii = 0; ii < temp.size(); ii++) {
                            String nombre = ((Canciones) temp.get(ii)).getNombree();
                            int duracion = ((Canciones) temp.get(ii)).getDuracion();
                            String dist = ((Canciones) temp.get(ii)).getDistri();
                            String compo = ((Canciones) temp.get(ii)).getCompositor();
                            String exclu = ((Canciones) temp.get(ii)).getExlusiva();
                            String prod = ((Canciones) temp.get(ii)).getNombreProductor();
                            String invi = ((Canciones) temp.get(ii)).getInvitado();
                            String original = ((Artista) artistas.get(row)).getName();
                            Object[] newrow = {nombre, duracion, dist, compo, exclu, prod, invi, original};
                            modelo2.addRow(newrow);
                        }
                        TableCanciones.setModel(modelo2);
                        ListarCanciones.setModal(true);
                        ListarCanciones.pack();
                        ListarCanciones.setLocationRelativeTo(origen);
                        ListarCanciones.setVisible(true);

                    } else {
                        JOptionPane.showMessageDialog(VerAlbum, "No tiene canciones disponibles");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(ListarArtistas, "Debe seleccionar algun album de la lista");
        }
    }//GEN-LAST:event_jButton15MouseClicked


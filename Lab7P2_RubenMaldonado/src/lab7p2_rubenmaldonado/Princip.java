    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        if (Table.getSelectedRow() >= 0) {
            DefaultTableModel modelo
                    = (DefaultTableModel) Table.getModel();
            Object name = modelo.getValueAt(Table.getSelectedRow(), 0);
            Object alias = modelo.getValueAt(Table.getSelectedRow(), 3);
            String n = name.toString();
            String a = alias.toString();
            try {
                for (int i = 0; i < artistas.size(); i++) {
                    String aux = ((Artista) artistas.get(i)).getName();
                    String aux2 = ((Artista) artistas.get(i)).getAlias();
                    if (n.equals(aux) && a.equals(aux2)) {
                        String fecha = ((Artista) artistas.get(i)).getName();
                        ArrayList<Album> temp = new ArrayList();
                        temp = ((Artista) artistas.get(i)).getAlbum();
                        auxi = temp;
                        auxiliar2 = i;

                        if (!temp.isEmpty()) {
                            String f = ((Album) temp.get(i)).getFecha();
                            String g = ((Album) temp.get(i)).getGenero();
                            String ff = ((Album) temp.get(i)).getFormato();
                            String alia = ((Album) temp.get(i)).getAliass();
                            Object[] newrow = {f, g, ff, alia, fecha};
                            DefaultTableModel modelo2
                                    = (DefaultTableModel) TableAlbum.getModel();
                            modelo2.addRow(newrow);
                            TableAlbum.setModel(modelo2);
                            Table.setModel(modelo);
                            VerAlbum.setModal(true);
                            VerAlbum.pack();
                            VerAlbum.setLocationRelativeTo(origen);
                            VerAlbum.setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(ListarArtistas, "Actualmente el artista no posee un album");
                        }

                    } else {
                        JOptionPane.showMessageDialog(ListarArtistas, "Debe seleccionar algun artista de la lista");
                    }
                }
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(VerAlbum, "Actualmente el artista no posee un album");
            }
        }
    }//GEN-LAST:event_jButton6MouseClicked

